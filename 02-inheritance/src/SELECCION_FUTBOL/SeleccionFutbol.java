
package SELECCION_FUTBOL;


public class SeleccionFutbol {
    
    String nombre;
    int edad;

    public SeleccionFutbol(String nombre, int edad) {
        this.nombre = nombre;
        this.edad = edad;
    }

    public String getNombre() {
        return nombre;
    }

    public int getEdad() {
        return edad;
    }
    
    
    
}
